    public void retrieve(){
        String ans = jAns.getText();
        
        String sql = "Select * From teachers_reg Where ans = '"+ans+"'";
        
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                jPass.setText(rs.getString("password"));
            }else{
                JOptionPane.showMessageDialog(null, "Answer is not matched. Try Again.");
                jAns.setText(null);
            }
        } catch (SQLException ex) {
            
        }
        
    }

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(conn != null){
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String date = sdf.format(jDate.getDate());
            
            String notice_no = jNoticeNo.getText();
            String notice = jNotice.getText();
            
            if(!date.isEmpty() && !notice_no.isEmpty() && !notice.isEmpty()){
                String sql = "Insert into notice(date, notice_no, notice) values" + "(?, ?, ?)";
                try {
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, date);
                    ps.setString(2, notice_no);
                    ps.setString(3, notice);
                    
                    int res = ps.executeUpdate();
                    
                    if(res >= 1){
                        JOptionPane.showMessageDialog(null, "One Notice Published.");
                        jDate.setDate(null);
                        jNoticeNo.setText(null);
                        jNotice.setText(null);
                    }else{
                        JOptionPane.showMessageDialog(null, "Notice Publication Failed.");
                    }
                            } catch (SQLException ex) {
                    Logger.getLogger(Admin_Home.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


public ArrayList<Notice> retrieveData(){
        ArrayList<Notice> al = null;
            al = new ArrayList<Notice>();
        try {
                  
            String n = "Select * From notice";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(n);
            
            Notice upnotice;
            
            while(rs.next()){
                upnotice = new Notice(rs.getString("date"), rs.getString("notice_no"), rs.getString("notice"));
                al.add(upnotice);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Admin_Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        return al;
    }
    public void showItemToFields(int index){
        jNoticeDate.setText(retrieveData().get(index).getDate());
        jNoticeNumber.setText(retrieveData().get(index).getNoticeNo());
        jUpdatedNotice.setText(retrieveData().get(index).getNotice());
    }
    public void fillTable(){
        ArrayList<Notice> al = retrieveData();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        Object[] row = new Object[3];
        
        for(Notice al1 : al){
            row[0] = al1.getDate();
            row[1] = al1.getNoticeNo();
            row[2] = al1.getNotice();
            
            model.addRow(row);
        }
    }

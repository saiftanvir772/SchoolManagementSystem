    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if((conn != null) && jRoll.getText() != null || jName.getText() != null || jUserName.getText() != null || jMail.getText() != null || jMob.getText() != null || jAdd.getText() != null){
            String roll = jRoll.getText();
            
            String sql = "Update students_reg set email = ?, mobile = ?, address = ? Where class_roll = ?";
            try {
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, jMail.getText());
                ps.setString(2, jMob.getText());
                ps.setString(3, jAdd.getText());
                ps.setString(4, roll);
                
                int res = ps.executeUpdate();
                
                if(res >= 1){
                    JOptionPane.showMessageDialog(null, "One Profile Updated");
                    jRoll.setText(null);
                    jName.setText(null);
                    jUserName.setText(null);
                    jMail.setText(null);
                    jMob.setText(null);
                    jAdd.setText(null);
                }else{
                    JOptionPane.showMessageDialog(null, "Update Failed.");
                    jRoll.setText(null);
                    jName.setText(null);
                    jUserName.setText(null);
                    jMail.setText(null);
                    jMob.setText(null);
                    jAdd.setText(null);
                }
            } catch (SQLException ex) {
                
            }
            
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed


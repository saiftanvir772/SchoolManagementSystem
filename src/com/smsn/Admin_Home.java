    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if(jNoticeDate.getText() != null || jNoticeNumber.getText() != null || jUpdatedNotice.getText() != null){
            String query = null;
            PreparedStatement ps = null;
            
            query = "Update notice set notice = ? Where notice_no = ?";
            try {
                ps = conn.prepareStatement(query);
                ps.setString(1, jUpdatedNotice.getText());
                ps.setString(2, jNoticeNumber.getText());
                
                int res = ps.executeUpdate();
                
                fillTable();
                
                if(res >= 1){
                    JOptionPane.showMessageDialog(null, "One Notice Updated.");
                        jNoticeDate.setText(null);
                        jNoticeNumber.setText(null);
                        jUpdatedNotice.setText(null);
                }else{
                    JOptionPane.showMessageDialog(null, "Notice Update Fail.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Admin_Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton8ActionPerformed


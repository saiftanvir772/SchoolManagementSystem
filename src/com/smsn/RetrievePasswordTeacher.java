    public void search(){
        String user = jUser.getText();
        
        String sql = "Select * From teachers_reg Where user_name = '"+user+"'";
        
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                jName.setText(rs.getString("name"));
                jQues.setText(rs.getString("ques"));
                
                rs.close();
                ps.close();
            }else{
                JOptionPane.showMessageDialog(null, "Invalid User Name");
                jUser.setText(null);
                jName.setText(null);
                jQues.setText(null);
            }
        } catch (SQLException ex) {
            
        }
        
    }

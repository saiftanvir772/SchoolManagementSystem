    private void jLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginActionPerformed
       String name = jUser.getText();
       String pass = jPass.getText();
       
       try{
           Statement st = conn.createStatement();
           
           String sql = "Select * From students_reg Where user_name = '"+name+"' and password = '"+pass+"'";
           
           ResultSet rs = st.executeQuery(sql);
           
           if(rs.isBeforeFirst()){
               new StudentHome().setVisible(true);
               this.setVisible(false);
           }else{
                JOptionPane.showMessageDialog(null, "Please Enter Valid User Name and Password");
                JOptionPane.showMessageDialog(null, "If you are a new user please register first");
                jUser.setText(null);
                jPass.setText(null);
           }
       }catch(SQLException | HeadlessException ex){
       
       }
    }//GEN-LAST:event_jLoginActionPerformed

